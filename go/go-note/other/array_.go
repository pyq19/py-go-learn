package main
import "fmt"

func main() {
    var a[4]int         // 元素自动初始化为零
    b := [4]int{2, 5}   // 未提供初始值的元素自动初始化为0
    c := [4]int{5, 3:10}// 可指定索引值位置初始化
    d := [...]int{1, 2, 3} // 编译器按初始化值数量确定数组长度
    e := [...]int{10, 3:100}   // 支持索引初始化，但注意数组长度与此有关
    fmt.Println(a, b, c, d, e)
}
// [0 0 0 0] [2 5 0 0] [5 0 0 10] [1 2 3] [10 0 0 100]
